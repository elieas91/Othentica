// Helper function to get country code by country code
export const getCountryCodeByCode = (countryCode) => {
  return countryCodeMap[countryCode] || '+971'; // Default to UAE
};

// Helper function to get country name by country code
export const getCountryNameByCode = (countryCode) => {
  const country = popularCountryCodes.find(c => c.code === countryCodeMap[countryCode]);
  return country ? country.country : 'Unknown';
};
